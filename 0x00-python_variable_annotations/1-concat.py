#!/usr/bin/env python3
"""
This module concatenate two strings str1 and str2.
"""


def concat(str1: str, str2: str) -> str:
    """
    A function that take to strings as arguments and concatenates them.

    Args:
        str1 (str): First string.
        str2 (str): Second string.
    """
    return str1 + str2
